package com.awl.tch.model;

import com.awl.tch.annotation.Column;
import com.awl.tch.annotation.Table;

@Table(name="TCH_CAPKEY")
public class CapKeyDTO {
	
	@Column(name="BANKCODE")
	private Integer bankCode;
	
	@Column(name="SCHEME")
	private String schemaName;

	@Column(name="EXPIRYDATE")
	private String expiryDate;
	
	@Column(name="RID")
	private String rid;;
	
	@Column(name="INDEX")
	private String index;
	
	@Column(name="EXPONENT")
	private String exponent;
	
	@Column(name="MODULAS")
	private String modulas;

	/**
	 * @return the bankCode
	 */
	public Integer getBankCode() {
		return bankCode;
	}

	/**
	 * @param bankCode the bankCode to set
	 */
	public void setBankCode(Integer bankCode) {
		this.bankCode = bankCode;
	}

	/**
	 * @return the schemaName
	 */
	public String getSchemaName() {
		return schemaName;
	}

	/**
	 * @param schemaName the schemaName to set
	 */
	public void setSchemaName(String schemaName) {
		this.schemaName = schemaName;
	}

	/**
	 * @return the expiryDate
	 */
	public String getExpiryDate() {
		return expiryDate;
	}

	/**
	 * @param expiryDate the expiryDate to set
	 */
	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}

	/**
	 * @return the rid
	 */
	public String getRid() {
		return rid;
	}

	/**
	 * @param rid the rid to set
	 */
	public void setRid(String rid) {
		this.rid = rid;
	}

	/**
	 * @return the index
	 */
	public String getIndex() {
		return index;
	}

	/**
	 * @param index the index to set
	 */
	public void setIndex(String index) {
		this.index = index;
	}

	/**
	 * @return the exponent
	 */
	public String getExponent() {
		return exponent;
	}

	/**
	 * @param exponent the exponent to set
	 */
	public void setExponent(String exponent) {
		this.exponent = exponent;
	}

	/**
	 * @return the modulas
	 */
	public String getModulas() {
		return modulas;
	}

	/**
	 * @param modulas the modulas to set
	 */
	public void setModulas(String modulas) {
		this.modulas = modulas;
	}
	
}
