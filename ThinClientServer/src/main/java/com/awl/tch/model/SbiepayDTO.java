package com.awl.tch.model;

import com.awl.tch.annotation.Column;
import com.awl.tch.annotation.Id;
import com.awl.tch.annotation.Table;

@Table(name = "TCH_SBIEPAY_REPORT" )
public class SbiepayDTO {

	@Column(name="S_RRN")
	private String rrn;
	
	@Column(name="S_ATRN")
	@Id
	private String atrn;
	
	@Column(name="S_AMOUNT")
	private String amount;
	
	@Column(name="S_TRASACTIONDATE")
	private String transactionDate;
	
	@Column(name="S_STATUS")
	private String status;
	
	@Column(name="S_PROCESSED")
	private String processed;
	
	@Column(name="S_PAYMENT_PROCESSOR")
	private String paymentProcessor;

	/**
	 * @return the rrn
	 */
	public String getRrn() {
		return rrn;
	}

	/**
	 * @param rrn the rrn to set
	 */
	public void setRrn(String rrn) {
		this.rrn = rrn;
	}

	/**
	 * @return the atrn
	 */
	public String getAtrn() {
		return atrn;
	}

	/**
	 * @param atrn the atrn to set
	 */
	public void setAtrn(String atrn) {
		this.atrn = atrn;
	}

	/**
	 * @return the amount
	 */
	public String getAmount() {
		return amount;
	}

	/**
	 * @param amount the amount to set
	 */
	public void setAmount(String amount) {
		this.amount = amount;
	}

	/**
	 * @return the transactionDate
	 */
	public String getTransactionDate() {
		return transactionDate;
	}

	/**
	 * @param transactionDate the transactionDate to set
	 */
	public void setTransactionDate(String transactionDate) {
		this.transactionDate = transactionDate;
	}

	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}

	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}

	/**
	 * @return the processed
	 */
	public String getProcessed() {
		return processed;
	}

	/**
	 * @param processed the processed to set
	 */
	public void setProcessed(String processed) {
		this.processed = processed;
	}

	/**
	 * @return the paymentProcessor
	 */
	public String getPaymentProcessor() {
		return paymentProcessor;
	}

	/**
	 * @param paymentProcessor the paymentProcessor to set
	 */
	public void setPaymentProcessor(String paymentProcessor) {
		this.paymentProcessor = paymentProcessor;
	}
	
	
}
