package com.awl.tch.model;

import com.awl.tch.annotation.Column;
import com.awl.tch.annotation.Id;
import com.awl.tch.annotation.Sequence;
import com.awl.tch.annotation.Table;

@Table(name="TCH_DETAIL_REPORT")
public class DetailReportDTO {
	
	@Id
	@Column(name = "D_ID")
	private Long id;
	
	@Column(name = "D_DISPLAY_MSG_PARAM")
	private String displayMessageParameter;
	
	@Column(name = "D_COLUMN_NAME")
	private String columnName;

	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the displayMessageParameter
	 */
	public String getDisplayMessageParameter() {
		return displayMessageParameter;
	}

	/**
	 * @param displayMessageParameter the displayMessageParameter to set
	 */
	public void setDisplayMessageParameter(String displayMessageParameter) {
		this.displayMessageParameter = displayMessageParameter;
	}

	/**
	 * @return the columnName
	 */
	public String getColumnName() {
		return columnName;
	}

	/**
	 * @param columnName the columnName to set
	 */
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	
}	
