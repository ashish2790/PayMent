package com.awl.tch.model;

import com.awl.tch.annotation.Column;
import com.awl.tch.annotation.Table;


@Table(name = "TCH_FEE_TABLE")
public class TCH_FEE_DTO {

	@Column(name = "F_ID")
	private int id;
	
	@Column(name = "F_PER")
	private String percentage;
	
	@Column(name = "F_FIXED")
	private String fixed;
	
	@Column(name = "F_MAX_AMT")
	private String maxAmount;
	
	@Column(name = "F_MIN_AMT")
	private String minAmount;
	
	@Column(name = "F_MID")
	private String mid;
	
	@Column(name = "F_CAL_METHOD")
	private String calMethod;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the percentage
	 */
	public String getPercentage() {
		return percentage;
	}

	/**
	 * @param percentage the percentage to set
	 */
	public void setPercentage(String percentage) {
		this.percentage = percentage;
	}

	/**
	 * @return the fixed
	 */
	public String getFixed() {
		return fixed;
	}

	/**
	 * @param fixed the fixed to set
	 */
	public void setFixed(String fixed) {
		this.fixed = fixed;
	}

	/**
	 * @return the maxAmount
	 */
	public String getMaxAmount() {
		return maxAmount;
	}

	/**
	 * @param maxAmount the maxAmount to set
	 */
	public void setMaxAmount(String maxAmount) {
		this.maxAmount = maxAmount;
	}

	/**
	 * @return the minAmount
	 */
	public String getMinAmount() {
		return minAmount;
	}

	/**
	 * @param minAmount the minAmount to set
	 */
	public void setMinAmount(String minAmount) {
		this.minAmount = minAmount;
	}

	/**
	 * @return the mid
	 */
	public String getMid() {
		return mid;
	}

	/**
	 * @param mid the mid to set
	 */
	public void setMid(String mid) {
		this.mid = mid;
	}

	/**
	 * @return the calMethod
	 */
	public String getCalMethod() {
		return calMethod;
	}

	/**
	 * @param calMethod the calMethod to set
	 */
	public void setCalMethod(String calMethod) {
		this.calMethod = calMethod;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "TCH_FEE_DTO [id=" + id + ", percentage=" + percentage
				+ ", fixed=" + fixed + ", maxAmount=" + maxAmount
				+ ", minAmount=" + minAmount + ", mid=" + mid + ", calMethod="
				+ calMethod + "]";
	}
	
}
