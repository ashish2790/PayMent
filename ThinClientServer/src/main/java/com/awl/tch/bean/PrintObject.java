package com.awl.tch.bean;

import com.google.gson.annotations.SerializedName;

public class PrintObject {
	
	@SerializedName(value="prtVlNm")
	private String printValueName;
	
	@SerializedName(value="prtVlDt")
	private String printValueData;
	
	@SerializedName(value="prtVlCnt")
	private String printValueCount;

	/**
	 * @return the printValueName
	 */
	public String getPrintValueName() {
		return printValueName;
	}

	/**
	 * @param printValueName the printValueName to set
	 */
	public void setPrintValueName(String printValueName) {
		this.printValueName = printValueName;
	}

	/**
	 * @return the printValueData
	 */
	public String getPrintValueData() {
		return printValueData;
	}

	/**
	 * @param printValueData the printValueData to set
	 */
	public void setPrintValueData(String printValueData) {
		this.printValueData = printValueData;
	}

	/**
	 * @return the printValueCount
	 */
	public String getPrintValueCount() {
		return printValueCount;
	}

	/**
	 * @param printValueCount the printValueCount to set
	 */
	public void setPrintValueCount(String printValueCount) {
		this.printValueCount = printValueCount;
	}
	
	
	
	
}
