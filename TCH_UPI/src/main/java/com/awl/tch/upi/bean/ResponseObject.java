package com.awl.tch.upi.bean;

import com.google.gson.annotations.SerializedName;

public class ResponseObject {

	@SerializedName(value="prgType")
	private String programType;
	
	@SerializedName(value="qrString")
	private String qrString;
	
	@SerializedName(value="txnID")
	private String transactionId;
	
	@SerializedName(value="trId")
	private String trId;
	
	@SerializedName(value="rrn")
	private String rrn;

	@SerializedName(value="authCode")
	private String authCode;
	
	@SerializedName(value="custVPA")
	private String customerVpa;
	
	@SerializedName(value="merchVPA")
	private String merchantVpa;


	/**
	 * @return the rrn
	 */
	public String getRrn() {
		return rrn;
	}

	/**
	 * @param rrn the rrn to set
	 */
	public void setRrn(String rrn) {
		this.rrn = rrn;
	}

	/**
	 * @return the authCode
	 */
	public String getAuthCode() {
		return authCode;
	}

	/**
	 * @param authCode the authCode to set
	 */
	public void setAuthCode(String authCode) {
		this.authCode = authCode;
	}

	/**
	 * @return the customerVpa
	 */
	public String getCustomerVpa() {
		return customerVpa;
	}

	/**
	 * @param customerVpa the customerVpa to set
	 */
	public void setCustomerVpa(String customerVpa) {
		this.customerVpa = customerVpa;
	}

	/**
	 * @return the merchantVpa
	 */
	public String getMerchantVpa() {
		return merchantVpa;
	}

	/**
	 * @param merchantVpa the merchantVpa to set
	 */
	public void setMerchantVpa(String merchantVpa) {
		this.merchantVpa = merchantVpa;
	}

	/**
	 * @return the programType
	 */
	public String getProgramType() {
		return programType;
	}

	/**
	 * @param programType the programType to set
	 */
	public void setProgramType(String programType) {
		this.programType = programType;
	}

	/**
	 * @return the qrString
	 */
	public String getQrString() {
		return qrString;
	}

	/**
	 * @param qrString the qrString to set
	 */
	public void setQrString(String qrString) {
		this.qrString = qrString;
	}

	/**
	 * @return the transactionId
	 */
	public String getTransactionId() {
		return transactionId;
	}

	/**
	 * @param transactionId the transactionId to set
	 */
	public void setTransactionId(String transactionId) {
		this.transactionId = transactionId;
	}

	/**
	 * @return the trId
	 */
	public String getTrId() {
		return trId;
	}

	/**
	 * @param trId the trId to set
	 */
	public void setTrId(String trId) {
		this.trId = trId;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "ResponseObject [programType=" + programType + ", qrString="
				+ qrString + ", transactionId=" + transactionId + ", trId="
				+ trId + ", rrn=" + rrn + ", authCode=" + authCode
				+ ", customerVpa=" + customerVpa + ", merchantVpa="
				+ merchantVpa + "]";
	}
}
